    @staticmethod
    def _sanitize_response(text: str) -> str:
        """–£–¥–∞–ª—è–µ—Ç –Ω–µ–∂–µ–ª–∞—Ç–µ–ª—å–Ω—ã–µ —Å–∏–º–≤–æ–ª—ã/–ø–∞—Ç—Ç–µ—Ä–Ω—ã –∏–∑ –æ—Ç–≤–µ—Ç–∞ –º–æ–¥–µ–ª–∏."""
