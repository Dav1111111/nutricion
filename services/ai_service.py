    async def format_nutrition_summary(self, nutrition_data: Dict[str, Any]) -> str:
        """
        –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ—Ç –¥–∞–Ω–Ω—ã–µ –æ –ø–∏—Ç–∞–Ω–∏–∏ –≤ –∫—Ä–∞—Å–∏–≤—É—é –∏ —á–∏—Ç–∞–µ–º—É—é —Å–≤–æ–¥–∫—É.

        Args:
            nutrition_data: –°–ª–æ–≤–∞—Ä—å —Å –¥–∞–Ω–Ω—ã–º–∏ –æ –ø–∏—Ç–∞—Ç–µ–ª—å–Ω–æ–π —Ü–µ–Ω–Ω–æ—Å—Ç–∏.

        Returns:
            –°—Ç—Ä–æ–∫–∞ —Å–æ —Å–≤–æ–¥–∫–æ–π –≤ —Ñ–æ—Ä–º–∞—Ç–µ Markdown.
        """
        try:
            name = nutrition_data.get('–Ω–∞–∑–≤–∞–Ω–∏–µ_–±–ª—é–¥–∞', '–ù–µ–∏–∑–≤–µ—Å—Ç–Ω–æ–µ –±–ª—é–¥–æ')
            calories = nutrition_data.get('–∫–∞–ª–æ—Ä–∏–∏')
            proteins = nutrition_data.get('–±–µ–ª–∫–∏')
            fats = nutrition_data.get('–∂–∏—Ä—ã')
            carbs = nutrition_data.get('—É–≥–ª–µ–≤–æ–¥—ã')
            
            summary = f"**üìä –°–≤–æ–¥–∫–∞ –ø–æ –±–ª—é–¥—É: \"{name}\"**\n"
            
            if calories:
                summary += f"üî• –ö–∞–ª–æ—Ä–∏–∏: **{calories:.0f} –∫–∫–∞–ª**\n"
            
            bju_parts = []
            if proteins is not None:
                bju_parts.append(f"–ë–µ–ª–∫–∏: {proteins:.1f} –≥")
            if fats is not None:
                bju_parts.append(f"–ñ–∏—Ä—ã: {fats:.1f} –≥")
            if carbs is not None:
                bju_parts.append(f"–£–≥–ª–µ–≤–æ–¥—ã: {carbs:.1f} –≥")

            if bju_parts:
                summary += f"‚öñÔ∏è –ë–ñ–£: " + " | ".join(bju_parts) + "\n"

            # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –ø—É—Å—Ç—É—é —Å—Ç—Ä–æ–∫—É, –µ—Å–ª–∏ –Ω–µ—Ç –¥–∞–Ω–Ω—ã—Ö –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è, –∫—Ä–æ–º–µ –Ω–∞–∑–≤–∞–Ω–∏—è
            if not bju_parts and not calories:
                return ""
                
            return summary.strip()
            
        except Exception as e:
            logger.error(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–∏ —Å–≤–æ–¥–∫–∏ –æ –ø–∏—Ç–∞–Ω–∏–∏: {e}")
            return ""

